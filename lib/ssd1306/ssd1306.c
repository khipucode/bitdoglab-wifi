// ── Envio de comando via I2C ───────────────────────────────
static void send_cmd(ssd1306_t *oled, uint8_t cmd) {
    uint8_t buf[2] = { 0x00, cmd };
    i2c_write_blocking(oled->i2c, oled->addr, buf, 2, false);
}

// ── Inicialização ──────────────────────────────────────────
void ssd1306_init(ssd1306_t *oled, uint8_t width, uint8_t height,
                  uint8_t addr, i2c_inst_t *i2c) {
    oled->width  = width;
    oled->height = height;
    oled->addr   = addr;
    oled->i2c    = i2c;
    memset(oled->buf, 0, sizeof(oled->buf));

    sleep_ms(100);

    send_cmd(oled, SSD1306_CMD_DISPLAY_OFF);
    send_cmd(oled, SSD1306_CMD_SET_MEM_ADDR); send_cmd(oled, 0x00); // horizontal
    send_cmd(oled, SSD1306_CMD_SET_MUX);      send_cmd(oled, height - 1);
    send_cmd(oled, SSD1306_CMD_SET_DISP_OFFSET); send_cmd(oled, 0x00);
    send_cmd(oled, SSD1306_CMD_SET_START_LINE);
    send_cmd(oled, SSD1306_CMD_SET_SEG_REMAP);
    send_cmd(oled, SSD1306_CMD_SET_COM_OUT_DIR);
    send_cmd(oled, SSD1306_CMD_SET_COM_PIN_CFG);
    send_cmd(oled, (height == 64) ? 0x12 : 0x02);
    send_cmd(oled, SSD1306_CMD_SET_CONTRAST);   send_cmd(oled, 0xCF);
    send_cmd(oled, SSD1306_CMD_ENTIRE_ON);
    send_cmd(oled, SSD1306_CMD_NORM_INV);
    send_cmd(oled, SSD1306_CMD_SET_DISP_CLK_DIV); send_cmd(oled, 0x80);
    send_cmd(oled, SSD1306_CMD_SET_PRECHARGE);     send_cmd(oled, 0xF1);
    send_cmd(oled, SSD1306_CMD_SET_VCOM_DESEL);    send_cmd(oled, 0x30);
    send_cmd(oled, SSD1306_CMD_SET_CHARGE_PUMP);   send_cmd(oled, 0x14);
    send_cmd(oled, SSD1306_CMD_DISPLAY_ON);
}

// ── Limpa buffer ───────────────────────────────────────────
void ssd1306_clear(ssd1306_t *oled) {
    memset(oled->buf, 0, sizeof(oled->buf));
}

// ── Envia buffer para o display ────────────────────────────
void ssd1306_show(ssd1306_t *oled) {
    int pages = oled->height / 8;

    send_cmd(oled, SSD1306_CMD_SET_COL_ADDR);
    send_cmd(oled, 0);
    send_cmd(oled, oled->width - 1);

    send_cmd(oled, SSD1306_CMD_SET_PAGE_ADDR);
    send_cmd(oled, 0);
    send_cmd(oled, pages - 1);

    int len = oled->width * pages;
    uint8_t tmp[len + 1];
    tmp[0] = 0x40;  // Co=0, D/C=1
    memcpy(tmp + 1, oled->buf, len);
    i2c_write_blocking(oled->i2c, oled->addr, tmp, len + 1, false);
}

// ── Pixel individual ───────────────────────────────────────
void ssd1306_draw_pixel(ssd1306_t *oled, int x, int y, bool on) {
    if (x < 0 || x >= oled->width || y < 0 || y >= oled->height) return;
    int byte_idx = (y / 8) * oled->width + x;
    int bit      = y % 8;
    if (on)
        oled->buf[byte_idx] |=  (1 << bit);
    else
        oled->buf[byte_idx] &= ~(1 << bit);
}

// ── Caractere (fonte 5x8, scale=1 → 6px wide com espaço) ──
void ssd1306_draw_char(ssd1306_t *oled, int x, int y, int scale, char c) {
    if (c < 32 || c > 126) c = '?';
    const uint8_t *glyph = font5x8[c - 32];
    for (int col = 0; col < 5; col++) {
        uint8_t column = glyph[col];
        for (int row = 0; row < 8; row++) {
            bool on = (column >> row) & 1;
            for (int sy = 0; sy < scale; sy++)
                for (int sx = 0; sx < scale; sx++)
                    ssd1306_draw_pixel(oled,
                        x + col * scale + sx,
                        y + row * scale + sy, on);
        }
    }
}

// ── String ─────────────────────────────────────────────────
void ssd1306_draw_string(ssd1306_t *oled, int x, int y, int scale, const char *str) {
    int cx = x;
    while (*str) {
        ssd1306_draw_char(oled, cx, y, scale, *str++);
        cx += 6 * scale;
        if (cx + 6 * scale > oled->width) break;
    }
}
